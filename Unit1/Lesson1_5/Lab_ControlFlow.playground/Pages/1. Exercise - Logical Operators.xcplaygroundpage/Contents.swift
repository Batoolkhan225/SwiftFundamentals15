/*:
## Exercise - Logical Operators

For each of the logical expressions below, print out what you think the resulting value will be (`true` or `false`). Then print out the actual expression to see if you were right. An example has been provided below.

    43 == 53
    print(false)
    print(43 == 53)
*/

// 1. `9 == 9`
print(true) // Expected: true
print(9 == 9) // Actual

// 2. `9 != 9`
print(false) // Expected: false
print(9 != 9) // Actual

// 3. `47 > 90`
print(false) // Expected: false
print(47 > 90) // Actual

// 4. `47 < 90`
print(true) // Expected: true
print(47 < 90) // Actual

// 5. `4 <= 4`
print(true) // Expected: true
print(4 <= 4) // Actual

// 6. `4 >= 5`
print(false) // Expected: false
print(4 >= 5) // Actual

// 7. `(47 > 90) && (47 < 90)`
print(false) // Expected: false
print((47 > 90) && (47 < 90)) // Actual

// 8. `(47 > 90) || (47 < 90)`
print(true) // Expected: true
print((47 > 90) || (47 < 90)) // Actual

// 9. `!true`
print(false) // Expected: false
print(!true) // Actual

